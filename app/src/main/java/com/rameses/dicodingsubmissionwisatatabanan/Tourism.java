package com.rameses.dicodingsubmissionwisatatabanan;

public class Tourism {
    private String tourism_name;
    private String tourism_location;
    private int tourism_detail;
    private int tourism_photo;

    public String getTourism_name() {
        return tourism_name;
    }

    public void setTourism_name(String tourism_name) {
        this.tourism_name = tourism_name;
    }

    public String getTourism_location() {
        return tourism_location;
    }

    public void setTourism_location(String tourism_location) {
        this.tourism_location = tourism_location;
    }

    public int getTourism_detail() {
        return tourism_detail;
    }

    public void setTourism_detail(int tourism_detail) {
        this.tourism_detail = tourism_detail;
    }

    public int getTourism_photo() {
        return tourism_photo;
    }

    public void setTourism_photo(int tourism_photo) {
        this.tourism_photo = tourism_photo;
    }
}
